/* Implement a function which convert the given boolean value into its string representation.

Note: Only valid inputs will be given.

PREP
P: b: boolean value to be converted
R: return the string version of the boolean
E: true would be converted and returned as "true"
P: truthy flag returns "true", falsy returns "false"
*/

function booleanToString(b){
    return b? "true" : "false";
}